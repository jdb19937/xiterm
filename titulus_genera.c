/*
 * colorem_elige — colorem pro charactere eligit.
 */
static uint32_t
colorem_elige(int ch)
{
    if (ch >= 0x41 && ch <= 0x5A)
        return COL_MAIUSC;   /* A-Z */
    if (ch >= 0x61 && ch <= 0x7A)
        return COL_MINUSC;   /* a-z */
    if (ch >= 0x30 && ch <= 0x39)
        return COL_NUMERI;    /* 0-9 */
    if (ch >= 0x20 && ch <= 0x7E)
        return COL_SIGNA;     /* signa */
    if (ch >= 0x80 && ch <= 0x87)
        return COL_CAUDEX;    /* caudices */
    if (ch >= 0x90 && ch <= 0x9A)
        return COL_CAPSA;     /* capsae */
    if (ch >= 0xB0 && ch <= 0xB5)
        return COL_CAPSA;     /* capsae duplices */
    if (ch >= 0xA0 && ch <= 0xAB)
        return COL_ALIA;      /* signa extensa */
    return COL_ALIA;
}

/*
 * atlanti_scribe — amplificat glyphos 4x et in atlante ponit.
 */
static void
atlanti_scribe(uint32_t *atlas)
{
    int ch, cy, cx, sy, sx;

    for (ch = 0; ch < NUM_GLYPHI; ch++) {
        int orig_x     = (ch % 16) * GLYPH_LAT;
        int orig_y     = (ch / 16) * GLYPH_ALT;
        uint32_t color = colorem_elige(ch);

        for (cy = 0; cy < CELL_ALT; cy++) {
            uint8_t linea = glyphi[ch][cy];
            for (cx = 0; cx < CELL_LAT; cx++) {
                if (linea & (0x80 >> cx)) {
                    for (sy = 0; sy < SCALA; sy++) {
                        for (sx = 0; sx < SCALA; sx++) {
                            int px = orig_x + cx * SCALA + sx;
                            int py = orig_y + cy * SCALA + sy;
                            atlas[py * ATLAS_LAT + px] = color;
                        }
                    }
                }
            }
        }
    }
}

int
main(int argc, char **argv)
{
    const char *via_exitus = "titulus_16.gif";
    uint32_t *atlas;
    pfr_gif_t *gif;

    if (argc > 1)
        via_exitus = argv[1];

    atlas = calloc(ATLAS_LAT * ATLAS_ALT, sizeof(uint32_t));
    if (!atlas) {
        fprintf(stderr, "memoria deficit\n");
        return 1;
    }

    atlanti_scribe(atlas);

    gif = pfr_gif_initia(via_exitus, ATLAS_LAT, ATLAS_ALT, 0, 1);
    if (!gif) {
        fprintf(stderr, "GIF creare non potest: %s\n", via_exitus);
        free(atlas);
        return 1;
    }

    pfr_gif_modum_pone(gif, PFR_QUANT_MEDIANA, PFR_DITHER_NULLUM);
    pfr_gif_tabulam_adde(gif, atlas);
    pfr_gif_fini(gif);

    free(atlas);
    fprintf(
        stderr, "%s generatus est (%dx%d, %d glyphi)\n",
        via_exitus, ATLAS_LAT, ATLAS_ALT, NUM_GLYPHI
    );
    return 0;
}
